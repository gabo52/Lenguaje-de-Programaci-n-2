package pe.edu.pucp.edusoftlp2.model;
public class Filiacion {
    private int idFiliacion;
    private String nombre;
    private String siglas;

    public int getIdFiliacion() {
        return idFiliacion;
    }

    public void setIdFiliacion(int idFiliacion) {
        this.idFiliacion = idFiliacion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getSiglas() {
        return siglas;
    }

    public void setSiglas(String siglas) {
        this.siglas = siglas;
    }
    
    
}
